#include "../../inc/libft.h"

void		ft_striteri(char *s, void (*f)(t_ull, char *))
{
	t_ull	i;

	i = 0;
	if (s && f)
		while (*(s + i))
		{
			f(i, s + i);
			++i;
		}
}
